package com.example.royalcash.model;

public class Seconde_person {
    String email,uid;

    public Seconde_person() {
    }

    public Seconde_person(String email, String uid) {
        this.email = email;
        this.uid = uid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
